#include<stdio.h>

/*

 Write a program in which to declare all data type like
integer ,double,float,long int and print value in specified format

*/
int main()
{
    //Initializing the variables
    int x=10;
    double y=15.69;
    float z=20.69;
    long int a =30;

    //Printing the variables
    printf("The value of integer is: %d \n",x);
    printf("The value of double is %f\n",y);
    printf("The value of the float is: %f \n",z);
    printf("The value of long int is: %d\n",a);

    return 0;


}